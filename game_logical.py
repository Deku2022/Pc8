import random

def загадати_число():
    """Генерує випадкове число від 1 до 100."""
    return random.randint(1, 100)

def перевірка_числа(введене_число, загадане_число):
    """Перевіряє введене число та повертає результат."""
    if введене_число < загадане_число:
        return "Загадане число більше."
    elif введене_число > загадане_число:
        return "Загадане число менше."
    else:
        return f"Вітаємо! Ви вгадали число {загадане_число}."

def гра():
    загадане_число = загадати_число()
    спроби = 0

    while True:
        спроби += 1
        введене_число = отримати_введене_число()

        результат = перевірка_числа(введене_число, загадане_число)
        вивести_результат(результат)

        if результат.startswith("Вітаємо!"):
            вивести_кількість_спроб(спроби)
            перезапустити_гру()

def отримати_введене_число():
    """Отримує введене число від користувача."""
    while True:
        введене_число = input("Спробуйте вгадати число від 1 до 100: ")

        try:
            введене_число = int(введене_число)
            return введене_число
        except ValueError:
            print("Введіть дійсне число!")

def вивести_результат(результат):
    """Виводить результат гри."""
    print(результат)

def вивести_кількість_спроб(спроби):
    """Виводить кількість спроб, яку зробив гравець."""
    print(f"Ви вгадали число за {спроби} спроб.")

def показати_підказку(введене_число, загадане_число):
    """Надає гравцеві підказку щодо розташування його числа відносно загаданого."""
    різниця = введене_число - загадане_число

    if різниця > 0:
        return f"Ваше число більше за загадане на {різниця}."
    elif різниця < 0:
        return f"Ваше число менше за загадане на {abs(різниця)}."
    else:
        return "Ваше число розташоване вірно."

def перезапустити_гру():
    """Дозволяє гравцеві перезапустити гру."""
    відповідь = input("Чи бажаєте ви перезапустити гру? (так/ні): ")

    if відповідь.lower() == "так":
        гра()
    else:
        print("Дякуємо за гру! До побачення.")

# Запуск гри
гра()
